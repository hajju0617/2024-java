package com.green.day1;

public class Print {
    public static void main(String[] args) {
        System.out.print("Hello!\n"); // "" 쌍따옴표는 문자열, String
        System.out.print("Hello!\n"); //
        System.out.print("Hello!\n"); // ctrl+D=한줄 복사

        System.out.println("안녕!"); // 자동으로 \n넣어줌
        System.out.println("안녕!");



        // psvm = public static void main(String[] args)
        // sout = System.out.println()
        // 파일명과 클래스 명이 같아야함

        // 객체-구성요소:속성(명사),메소드(동사)
        // static은 특별하다 why? 객체와 상관없다
        // 여러줄 선택 후 ctrl + / 하면 주석처리
    }
}
