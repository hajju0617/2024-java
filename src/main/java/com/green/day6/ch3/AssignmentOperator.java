package com.green.day6.ch3;

public class AssignmentOperator {
    public static void main(String[] args) {
        int x, y;
        // left value( 저장할 수 있는 공간 ex.변수,배열...)
        // right value ( 연산식, 변수, 리터럴)
        x = y = 3;
        System.out.println(x);
        System.out.println(y);
    }
}
