package com.green.day3;

public class MultipleForLoop2 {
    public static void main(String[] args) {


        for (int j=0; j<5; j++) {
            for (int i=0; i<5; i++) {
                System.out.printf("%d - %d\n", j, i);

            }
            System.out.println("--------------");

        }
    }

}