package com.green.day2;

public class Var {

    //Primitive Type (8개) = 정수형(byte,short,int,long),실수형(float,double),불린형(boolean),문자형(char)
                     //정수형의 크기 1     2    4    8바이트 ( 1byte = 8bit)
    // Reference Type >> 개수가 무한대 >> 클래스를 만듦

    public static void main(String[] args){
        int num;
        num = 10;
        System.out.println(num);

        num = 20;
        System.out.println(num);

        num = 20 + 30;
        System.out.println(num);

        num = 10 * 40;
        System.out.println(num);

        num = 10 / 2;
        System.out.println(num);

        num = 10 - 3;
        System.out.println(num);

        num = 10 % 2;
        System.out.println(num);

        num = 9 % 2;
        System.out.println(num);

    }
}

